/* ------------------------lcd display function------------------------ */

/*    函数说明：在指定位置画点
      入口数据：x,y 画点坐标
                color 点的颜色
      返回值：  无
*/
void LCD_DrawPoint(uint16_t x,uint16_t y,uint16_t color)
{
	LCD_Address_Set(x,y,x,y);//设置光标位置 
	SPI_WrDat16(color);
}

/*    函数说明：在指定区域填充颜色
      入口数据：xsta,ysta   起始坐标
                xend,yend   终止坐标
				color       要填充的颜色
      返回值：  无
*/
void LCD_Fill(uint16_t xsta,uint16_t ysta,uint16_t xend,uint16_t yend,uint16_t color)
{
	uint16_t i,j; 
	LCD_Address_Set(xsta,ysta,xend-1,yend-1);//设置显示范围
	for(i=ysta;i<yend;i++)
	{													   	 	
		for(j=xsta;j<xend;j++)
		{
			SPI_WrDat16(color);
		}
	} 					  	    
}

/* ------------------------lcd show character function------------------------ */

/*    函数说明：显示单个字符
      入口数据：x,y 画点坐标
                num 要显示的字符
                fc 字的颜色
                bc 字的背景色
                sizey 字号
                mode:  0非叠加模式  1叠加模式
      返回值：  无
*/
void LCD_ShowChar(uint16_t x,uint16_t y,uint8_t num,uint16_t fc,uint16_t bc,uint8_t sizex,uint8_t mode)
{
	uint8_t temp, sizey, t, m=0;
	uint16_t i;
	uint16_t x0=x;
	sizey = sizex*4 - 16;
	num=num-' ';    //得到偏移后的值
	if (x + sizex > 160)
	{
		x = 0;
		y += sizey;
		if (y > 80)
			return;
	}
	LCD_Address_Set(x, y, x+sizex-1, y+sizey-1);  //设置光标位置 
	for(i = 0; i < sizey; i++)
	{ 
//		if (sizex == 6)
//			temp = F6x8[num][i];		       //调用6x8字体
//		else if (sizex == 8)
		temp = ascii_1608[num][i];		 //调用8x16字体
//		else if(sizey==24)temp=ascii_2412[num][i];		 //调用12x24字体
//		else if(sizey==32)temp=ascii_3216[num][i];		 //调用16x32字体
//		else return;
		for(t=0;t<8;t++)
		{
			if (!mode)//非叠加模式
			{
				if (temp&(0x01<<t))
					SPI_WrDat16(fc);
				else 
					SPI_WrDat16(bc);
				m++;
				if (m % sizex==0)
				{
					m=0;
					break;
				}
			}
			else//叠加模式
			{
				if (temp&(0x01<<t))
					LCD_DrawPoint(x,y,fc);//画一个点
				x++;
				if ((x-x0) == sizex)
				{
					x = x0;
					y++;
					break;
				}
			}
		}
	}   	 	  
}

/*    函数说明：显示字符串
      入口数据：x,y显示坐标
                *p 要显示的字符串
                fc 字的颜色
                bc 字的背景色
                sizey 字号
                mode:  0非叠加模式  1叠加模式
      返回值：  无
*/
void LCD_ShowString(uint16_t x,uint16_t y,const uint8_t *p,uint16_t fc,uint16_t bc,uint8_t sizex,uint8_t mode)
{         
	while(*p!='\0')
	{       
		LCD_ShowChar(x,y,*p,fc,bc,sizex,mode);
		x += sizex;
		p++;
	}  
}
