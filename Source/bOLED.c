void OLED_Fill(unsigned int usData)
{
    unsigned char y,x;

#if defined(CHIP_SSD1331) || defined(CHIP_SSD1351) || defined(CHIP_ST7735)
    OLED_Address_Set(0,0,OLED_WIDTH - 1,OLED_HIGH-1);
    for(x=0; x < OLED_HIGH; x++)
    {
        for (y=0; y < OLED_WIDTH; y++)
        {
            SPI_WrDat(usData >> 8);
            SPI_WrDat(usData);
        }
    }
#else
	for(y=0; y< (OLED_LINE_MAX + LINE_HEIGHT); y++)
	{
		SPI_WrCmd(0xb0+y);
        #ifdef CHIP_SH1106
        SPI_WrCmd(0x02);
        #else 
		SPI_WrCmd(0x00);
        #endif
		SPI_WrCmd(0x10);
		for(x=0;x<OLED_WIDTH;x++)
			SPI_WrDat(usData);
	}
#endif
}

