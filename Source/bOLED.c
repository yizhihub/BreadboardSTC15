void SPI_WrDat(unsigned char dat)
{
  unsigned char i=8;
//  OLED_CS_0;
//  OLED_DC_1;
//  __NOP();__NOP();__NOP();__NOP(); // yanshi
//  OLED_SCL_0;;
//  __NOP();__NOP();__NOP();__NOP(); // yanshi
  while(i--)
  {
    if(dat&0x80)            // 注意此处若为51单片机，采用CY位的方法速度略有提升。f = 340KHz -> 390KHz @12MHz
      OLED_SDA_1;
    else
      OLED_SDA_0;
//    __NOP();__NOP();// yanshi
    OLED_SCL_1;
//    __NOP();__NOP();// yanshi
    OLED_SCL_0;   
//    __NOP();__NOP();// yanshi
    dat<<=1;
  }
//  OLED_CS_1;
}
void SPI_WrCmd(unsigned char cmd)
{
  unsigned char i;
//  OLED_CS_0;
  OLED_DC_0;;
//  __NOP();__NOP();__NOP();__NOP(); // yanshi
//  OLED_SCL_0;;
//  __NOP();__NOP();__NOP();__NOP();// yanshi
  for(i=0;i<8;i++)
  {
   if(cmd&0x80)
      OLED_SDA_1;
   else
      OLED_SDA_0;
//   __NOP();__NOP();// yanshi 
   OLED_SCL_1;
//   __NOP();__NOP();// yanshi   
   OLED_SCL_0;
//   __NOP();__NOP();// yanshi  
   cmd<<=1;;  
  }
  OLED_DC_1;;
//  OLED_CS_1;
}
void OLED_Set_Pos(unsigned char x, unsigned char y)
{ 
  #ifdef CHIP_SH1106
  x += 2;
  #endif
  SPI_WrCmd(0xb0+y);
  SPI_WrCmd(x&0x0f);
  SPI_WrCmd(((x&0xf0)>>4)|0x10);
} 
void OLED_PutPixel(uint8_t x,uint8_t y)
{
    SPI_WrCmd(0xb0+7-y/8);   //0xb0+0~7表示页0~页7?
    #ifdef CHIP_SH1106    //0x00+0~16表示将128列分成16组其地址在某组中的第几列
    SPI_WrCmd(0x02 + x%16);
    #else 
    SPI_WrCmd(0x00 + x%16);
    #endif  
    SPI_WrCmd(0x10 + x/16);     //0x10+0~16表示将128列分成16组其地址所在第几组
    SPI_WrDat(0x80>>(y%8));
}
