/**
*********************************************************************************************************
** @nameis OLED_PutChar
** @effect 写一个字符
** @import x 显示的列坐标 y 显示的行坐标 wan 待写字符 ucYn 字体颜色(单色OLED屏幕0为反显，1为正常显示)
** @export none
** @return none
** @create yizhi 2023.03.27
** @modify  
*********************************************************************************************************/
void OLED_PutChar(uint8_t x,uint8_t y,uint8_t wan, u8 ucSize, uint16_t ucYn)
{   
#ifdef OLED_COLOR
    u8 pos,t,ucHeight;
    u32 ulTemp;
#else
    u8 i, j, ucTemp;
#endif
    if(x > (OLED_WIDTH - ucSize))   return; 
    if (ucSize <= 8) {
        wan = wan-' ';
    } else if (ucSize == 16){
        if (wan >= '0' && wan <= '9') {
            wan = wan - '0';
        } else if (wan == 127)  {                                   /* 摄氏度 */
            wan = 10;
        } else if (wan == 128) {
            wan = 11;
        } else if (wan == 'V') {
            wan = 12;
        } else if (wan == 'A') {
            wan = 13;
        } else if (wan == 'm') {
            wan = 14;
        } else if (wan == '.') {
            wan = 15;
        } else if (wan == '-') {
            wan = 16;
        } else if (wan == ':') {
            wan = 17;
        } else {
            return;                                                 /* 不支持显示的字符 */
        }
    }  else {
        return;                                                     /* 不支持的字号     */
    }
#ifdef OLED_COLOR
    OLED_Address_Set(x, y, x + ucSize - 1, y + (ucSize << 1 & 0xF8) - 1);/* set refresh windows                  */
    for(pos = 0; pos < ucSize; pos++)                                   /* ucSize also represent character width*/
    {
        if (ucSize == 6) {
     #ifdef FEATURE_F6x8
            ulTemp = F6x8[wan][pos];                                    /* 调用0608字体  */ 
     #else   
            ulTemp = 0xCC;
     #endif 
            ucHeight = 8;                                               /* ucHeight represent character Height */
        } else if (ucSize == 8) {
    #ifdef  FEATURE_F8x16
            ulTemp = (F8X16[wan][pos + 8] << 8) | F8X16[wan][pos]; /* 调用1608字体 */
    #else 
            ulTemp = 0xCCCC;                                            /* 显示斑马纹 */
    #endif
            ucHeight = 16;                                              /* ucHeight represent character Height */
        } else if (ucSize == 16) {
    #ifdef  FEATURE_F16x32
            ulTemp = (F16X32[wan][pos + 48] << 24) | (F16X32[wan][pos + 32] << 16) |
                     (F16X32[wan][pos + 16] << 8 ) | (F16X32[wan][pos]);/* 调用3216字体           */
    #else 
            ulTemp = 0xCCCCCCCC;                                        /* 显示斑马纹 */
    #endif
            ucHeight = 32;                                              /* ucHeight represent character Height */
        } else {
            return;
        }
        for (t = 0; t < ucHeight; t++)
        {
            if (ulTemp & 0x01) {
                SPI_WrDat(ucYn>>8); SPI_WrDat(ucYn);//LCD_DrawColor(ucYn);
            } else {
                SPI_WrDat(BACK_COLOR>>8); SPI_WrDat(BACK_COLOR);//LCD_DrawColor(BACK_COLOR);
            }
            ulTemp >>= 1;
        }
    }

#else
    for (j = 0; j < (ucSize >> 2); j++) {
        OLED_Set_Pos(x, y + j);
        for(i=0; i < ucSize; i++) {
            if (ucSize == 6) {
    #ifdef  FEATURE_F6x8
                ucTemp = F6x8[wan][i];
    #else        
                ucTemp = 0xCC;
    #endif
            } else if (ucSize == 8) {
    #ifdef  FEATURE_F8x16
                ucTemp = F8X16[wan][i + (ucSize * j)];
    #else 
                ucTemp = 0xCC;
    #endif
            } else if (ucSize == 16) {
    #ifdef      FEATURE_F16x32
                ucTemp = F16X32[wan][i + (ucSize * j)];/* 调用3216字体           */
    #else 
                ucTemp = 0xCC;                                        /* 显示斑马纹 */
    #endif
            } else {
                return;
            }
            ucYn ? SPI_WrDat(ucTemp) : SPI_WrDat(~ucTemp);
        }
    }
#endif
}
//==m============================================================
//函数名：OLED_P8x16Str(uint8_t x,uint8_t y,uint8_t *p)
//功能描述：写入一组标准ASCII字符串
//参数：显示的位置（x,y），y为页范围0～7，要显示的字符串
//返回：无   yn=1  正常显示  yn=0 反黑显示 
//==============================================================  
void OLED_PutStr(uint8_t x,uint8_t y,uint8_t ch[], u8 ucSize, uint16_t ucYn)
{
    uint8_t j=0, wan;
    
    if (ucYn == 1) ucYn = FRONT_COLOR;
    while (ch[j]!='\0')
    {    
        wan = ch[j];
        if(x > (OLED_WIDTH - ucSize))
        {
            x=0;
#ifdef      OLED_COLOR
            y +=  (ucSize << 1) & 0x18;  /*  y += (ucSize == 6 ? (LINE_HEIGHT >> 1) : LINE_HEIGHT); which is faster ? */
#else 
            y += ((ucSize & 0x08) ? 2 : 1);   
#endif
            if (y > OLED_LINE_MAX)       // 此处有一个待处理的BUG，若显示字体为6号，最后一行书写不了。
                return;
        }
        
       /* 
        * 特殊单位符号处理 ℃=0xA1E6
        */
        if (wan == 0xA1) wan = 127;
        if (wan == 0xE6) wan = 128;                                     /* 摄氏度                       */
        OLED_PutChar(x, y, wan, ucSize, ucYn);
        x += ucSize;
        j++; 
    }
}

