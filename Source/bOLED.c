    /*
     * stiumulate the clock
     */
void timeClockStep(RTC_Time_s *ptTime)
{
     if (++ptTime->second == 60) {
       if (++ptTime->minute == 60) { 
           if (++ptTime->hour   == 24) {
               ptTime->date++;   
               ptTime->hour   = 0;
           }
           ptTime->minute = 0;
       }               
       ptTime->second = 0;
    }
}



